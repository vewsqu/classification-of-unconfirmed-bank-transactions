def blend_predictions(catboost_file="submission_catboost_champion.csv", other_file="submission_lightgbm.csv"):
    if not os.path.exists(other_file):
        log_step(f"Файл {other_file} не найден. Пропускаем блендинг.")
        return

    log_step(f"Смешиваем {catboost_file} и {other_file}...")
    sub1 = pd.read_csv(catboost_file)
    sub2 = pd.read_csv(other_file)

    sub1 = sub1.sort_values('event_id').reset_index(drop=True)
    sub2 = sub2.sort_values('event_id').reset_index(drop=True)

    blend = sub1.copy()

    # Взвешенное среднее (CatBoost 60% / Другая модель 40%)
    blend['predict'] = (sub1['predict'] * 0.6) + (sub2['predict'] * 0.4)

    output_name = "submission_final_blend.csv"
    blend.to_csv(output_name, index=False)
    log_step(f"✅ Успешно смешали! Файл сохранен как {output_name}.")

