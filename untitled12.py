# 3. ИНЖЕНЕРИЯ ПРИЗНАКОВ (POLARS)
# ==========================================
def prepare_features_full(df_lazy, hist_stats_lazy, hist_mcc_lazy, hist_device_lazy, w2v_lazy, user_profile_lazy, vector_size=32):
    df_lazy = df_lazy.with_columns([
        pl.col("operaton_amt").is_null().cast(pl.Int8).alias("is_non_financial"),
        pl.col("mcc_code").fill_null("-1").cast(pl.String),
        pl.col("device_system_version").fill_null("UNK").cast(pl.String),
        pl.col("event_dttm").str.to_datetime().alias("event_dttm_dt"),
        pl.col("operaton_amt").cast(pl.Float32).fill_null(0)
    ]).with_columns([
        (pl.col("event_dttm_dt").dt.hour() // 6).cast(pl.String).alias("time_bin"),
        pl.col("operaton_amt").log1p().cast(pl.Int32).cast(pl.String).alias("amt_bin")
    ]).with_columns([
        pl.concat_str([pl.col("mcc_code"), pl.col("amt_bin"), pl.col("time_bin")], separator="_").alias("complex_token")
    ])

    df_joined = df_lazy.join(hist_stats_lazy, on="customer_id", how="left")
    df_joined = df_joined.join(hist_mcc_lazy, on=["customer_id", "mcc_code"], how="left")
    df_joined = df_joined.join(hist_device_lazy, on=["customer_id", "device_system_version"], how="left")
    df_joined = df_joined.join(w2v_lazy, on="complex_token", how="left")
    df_joined = df_joined.join(user_profile_lazy, on="customer_id", how="left")

    dot_expr = pl.sum_horizontal([pl.col(f"token_w2v_{i}").fill_null(0) * pl.col(f"user_profile_w2v_{i}").fill_null(0) for i in range(vector_size)])
    norm_tok_expr = pl.sum_horizontal([pl.col(f"token_w2v_{i}").fill_null(0)**2 for i in range(vector_size)]).sqrt()
    norm_usr_expr = pl.sum_horizontal([pl.col(f"user_profile_w2v_{i}").fill_null(0)**2 for i in range(vector_size)]).sqrt()

    df_joined = df_joined.with_columns([(dot_expr / (norm_tok_expr * norm_usr_expr + 1e-6)).cast(pl.Float32).alias("w2v_cosine_similarity")])
    df_joined = df_joined.with_columns(
        [pl.col(f"token_w2v_{i}").fill_null(0).cast(pl.Float32) for i in range(vector_size)] +
        [pl.col(f"user_profile_w2v_{i}").fill_null(0).cast(pl.Float32) for i in range(vector_size)]
    )

    return (
        df_joined
        .with_columns([
            pl.col("last_hist_dttm").str.to_datetime().alias("last_hist_dttm_dt"),
            pl.col("user_mcc_count").fill_null(0).cast(pl.Int32),
            pl.col("user_device_count").fill_null(0).cast(pl.Int32),
            (pl.col("device_system_version") == "UNK").cast(pl.Int8).alias("is_device_missing"),
            pl.col("user_median_hour").fill_null(12).cast(pl.Float32),
        ])
        .sort(["customer_id", "event_dttm_dt"])
        .with_columns([
            pl.col("event_type_nm").fill_null("UNK").cast(pl.String),
            pl.col("event_desc").fill_null("UNK").cast(pl.String),
            pl.col("channel_indicator_type").fill_null("UNK").cast(pl.String),
            pl.col("channel_indicator_sub_type").fill_null("UNK").cast(pl.String),
            pl.col("pos_cd").fill_null("UNK").cast(pl.String),
            pl.col("operating_system_type").fill_null("UNK").cast(pl.String),
            pl.col("timezone").fill_null("UNK").cast(pl.String),
            pl.col("phone_voip_call_state").fill_null(0).cast(pl.Int8),
            pl.col("developer_tools").cast(pl.String).fill_null("UNK"),
            pl.col("web_rdp_connection").cast(pl.String).fill_null("UNK"),
            pl.col("compromised").cast(pl.String).fill_null("UNK"),
            pl.col("screen_size").str.split("x").list.get(0).cast(pl.Float32, strict=False).fill_null(0).alias("screen_w"),
            pl.col("event_dttm_dt").dt.hour().cast(pl.Int8).alias("hour"),
            pl.col("event_dttm_dt").dt.weekday().cast(pl.Int8).alias("day_of_week"),
            pl.concat_str([pl.col("mcc_code"), pl.lit("_"), pl.col("event_desc")]).alias("mcc_event_desc"),
            pl.concat_str([pl.col("mcc_code"), pl.lit("_"), pl.col("device_system_version")]).alias("mcc_device"),
            pl.col("operaton_amt").log1p().alias("operaton_amt_log"),
            (pl.col("event_dttm_dt").dt.hour() < 6).cast(pl.Int8).alias("is_night_transaction"),
            (pl.col("timezone") != pl.col("timezone").shift(1).over("customer_id")).cast(pl.Int8).fill_null(0).alias("is_timezone_changed"),
            pl.col("event_dttm_dt").diff().dt.total_seconds().over("customer_id").alias("diff_internal"),
            (pl.col("operaton_amt").cum_sum().over("customer_id") / pl.col("operaton_amt").cum_count().over("customer_id")).cast(pl.Float32).fill_null(0).alias("user_avg_amt"),
            (pl.col("user_mcc_count") == 0).cast(pl.Int8).alias("is_new_mcc"),
            (pl.col("user_device_count") == 0).cast(pl.Int8).alias("is_new_device"),
        ])
        .with_columns([
            pl.when(pl.col("diff_internal").is_null())
            .then((pl.col("event_dttm_dt") - pl.col("last_hist_dttm_dt")).dt.total_seconds())
            .otherwise(pl.col("diff_internal"))
            .fill_null(999999).cast(pl.Float32).alias("sec_since_last")
        ])
        .with_columns([
            (pl.col("operaton_amt") / (pl.col("sec_since_last") + 1)).cast(pl.Float32).alias("amt_per_sec"),
            (pl.col("operaton_amt") / (pl.col("old_avg_amt") + 1)).cast(pl.Float32).alias("amt_ratio_old"),
            (pl.col("operaton_amt") / (pl.col("user_avg_amt") + 1)).cast(pl.Float32).alias("amt_ratio_user"),
            (pl.col("operaton_amt") / (pl.col("old_max_amt") + 1)).cast(pl.Float32).alias("amt_ratio_max"),
            (pl.col("event_dttm_dt").dt.hour() - pl.col("user_median_hour")).abs().cast(pl.Float32).fill_null(0).alias("hour_diff_from_median"),
            ((pl.col("user_device_count") == 0) & (pl.col("sec_since_last") < 300)).cast(pl.Int8).alias("is_fast_new_device"),
            ((pl.col("user_mcc_count") == 0) & (pl.col("operaton_amt") > pl.col("old_avg_amt") * 3)).cast(pl.Int8).alias("is_large_new_mcc"),
        ])
    )

