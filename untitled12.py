# 5. ОСНОВНОЙ ПАЙПЛАЙН (MAIN)
# ==========================================
def main():
    w2v_lazy, w2v_cols, user_profile_lazy, user_w2v_cols = train_mcc_word2vec(vector_size=32)

    features = [
        "operaton_amt", "operaton_amt_log", "amt_per_sec", "amt_ratio_old", "amt_ratio_user",
        "amt_ratio_max", "user_mcc_count", "user_device_count", "is_new_mcc", "is_new_device",
        "hour", "day_of_week", "screen_w", "phone_voip_call_state", "developer_tools",
        "web_rdp_connection", "compromised", "sec_since_last", "user_avg_amt", "old_avg_amt",
        "old_max_amt", "old_trans_count", "is_non_financial", "is_device_missing",
        "is_night_transaction", "is_timezone_changed", "event_type_nm", "event_desc",
        "channel_indicator_type", "channel_indicator_sub_type", "mcc_code", "pos_cd",
        "operating_system_type", "device_system_version", "timezone", "mcc_event_desc", "mcc_device",
        "user_median_hour", "hour_diff_from_median", "is_fast_new_device", "is_large_new_mcc",
        "w2v_cosine_similarity"
    ] + w2v_cols + user_w2v_cols

    cat_features = [
        "event_type_nm", "event_desc", "channel_indicator_type", "channel_indicator_sub_type",
        "mcc_code", "pos_cd", "operating_system_type", "device_system_version", "timezone",
        "mcc_event_desc", "mcc_device", "developer_tools", "web_rdp_connection", "compromised"
    ]

    log_step("Шаг 1: Агрегация истории...")
    hist_lazy_train = get_history_lazy(PRETRAIN_FILES)
    hist_stats_train = hist_lazy_train.group_by("customer_id").agg([
        pl.col("operaton_amt").mean().cast(pl.Float32).alias("old_avg_amt"),
        pl.col("operaton_amt").max().cast(pl.Float32).alias("old_max_amt"),
        pl.col("event_id").len().cast(pl.Int32).alias("old_trans_count"),
        pl.col("event_dttm").max().alias("last_hist_dttm"),
        pl.col("event_dttm").str.to_datetime().dt.hour().median().cast(pl.Float32).alias("user_median_hour")
    ]).collect()
    hist_mcc_train = hist_lazy_train.group_by(["customer_id", "mcc_code"]).agg(pl.len().cast(pl.Int32).alias("user_mcc_count")).collect()
    hist_device_train = hist_lazy_train.group_by(["customer_id", "device_system_version"]).agg(pl.len().cast(pl.Int32).alias("user_device_count")).collect()

    log_step("Шаг 2: Подготовка Train...")
    train_dfs = []
    for file_path in TRAIN_FILES:
        chunk_lazy = pl.scan_parquet(file_path).unique(subset=["event_id"], maintain_order=True).join(
            pl.scan_parquet(TRAIN_LABELS).unique(subset=["event_id"], maintain_order=True), on="event_id", how="left"
        )
        chunk_features = prepare_features_full(
            chunk_lazy, hist_stats_train.lazy(), hist_mcc_train.lazy(), hist_device_train.lazy(),
            w2v_lazy, user_profile_lazy, vector_size=32
        ).with_columns([pl.col("target").fill_null(-1).alias("target_orig")])

        chunk_collected = chunk_features.filter(
            (pl.col("target_orig") != -1) | (pl.col("event_id").hash(seed=42) % 100 < 8)
        ).collect().with_columns([
            pl.when(pl.col("target_orig") == 1).then(1.0).otherwise(0.0).alias("target_bin")
        ]).fill_null(0).sort("event_dttm")
        train_dfs.append(chunk_collected)
        gc.collect()

    train_df = pl.concat(train_dfs).sort("event_dttm")
    del train_dfs; gc.collect()

    split_idx = int(len(train_df) * 0.8)
    train_pd = train_df.select(features + ["target_bin"]).to_pandas()
    del train_df; gc.collect()

    X_train = train_pd.iloc[:split_idx].drop(columns=["target_bin"])
    y_train = train_pd.iloc[:split_idx]["target_bin"]
    X_val = train_pd.iloc[split_idx:].drop(columns=["target_bin"])
    y_val = train_pd.iloc[split_idx:]["target_bin"]

    for col in cat_features:
        X_train[col] = X_train[col].astype('category')
        X_val[col] = X_val[col].astype('category')

    train_pool = Pool(data=X_train, label=y_train, cat_features=cat_features)
    val_pool = Pool(data=X_val, label=y_val, cat_features=cat_features)
    del X_train, y_train; gc.collect()

    log_step("Подготовка Теста...")
    hist_lazy_test = pl.concat([get_history_lazy(PRETRAIN_FILES), get_history_lazy(TRAIN_FILES), get_history_lazy(PRETEST_FILE)])
    hist_stats_test = hist_lazy_test.group_by("customer_id").agg([
        pl.col("operaton_amt").mean().cast(pl.Float32).alias("old_avg_amt"),
        pl.col("operaton_amt").max().cast(pl.Float32).alias("old_max_amt"),
        pl.col("event_id").len().cast(pl.Int32).alias("old_trans_count"),
        pl.col("event_dttm").max().alias("last_hist_dttm"),
        pl.col("event_dttm").str.to_datetime().dt.hour().median().cast(pl.Float32).alias("user_median_hour")
    ]).collect()
    hist_mcc_test = hist_lazy_test.group_by(["customer_id", "mcc_code"]).agg(pl.len().cast(pl.Int32).alias("user_mcc_count")).collect()
    hist_device_test = hist_lazy_test.group_by(["customer_id", "device_system_version"]).agg(pl.len().cast(pl.Int32).alias("user_device_count")).collect()

    test_df_polars = prepare_features_full(
        pl.scan_parquet(TEST_FILE).unique(subset=["event_id"], maintain_order=True),
        hist_stats_test.lazy(), hist_mcc_test.lazy(), hist_device_test.lazy(),
        w2v_lazy, user_profile_lazy, vector_size=32
    ).collect().fill_null(0)

    test_event_ids = test_df_polars["event_id"].to_numpy()
    X_test = test_df_polars.select(features).to_pandas()
    del test_df_polars; gc.collect()

    for col in cat_features:
        X_test[col] = X_test[col].astype('category')

    log_step("🔥 ОБУЧЕНИЕ CATBOOST...")
    model_cb = CatBoostClassifier(
        iterations=3500,
        learning_rate=0.03,
        depth=7,
        l2_leaf_reg=5,
        eval_metric='PRAUC',
        task_type="GPU",
        early_stopping_rounds=300,
        verbose=200,
        scale_pos_weight=5.0,
        bagging_temperature=1.0,
        random_seed=42
    )

    model_cb.fit(train_pool, eval_set=val_pool)

    log_step("Расчет метрики...")
    val_preds = model_cb.predict_proba(X_val)[:, 1]
    local_pr_auc = average_precision_score(y_val, val_preds)
    log_step(f"🎯 ИТОГОВЫЙ ЛОКАЛЬНЫЙ PR-AUC: {local_pr_auc:.6f}")

    log_step("Предсказание на Test...")
    final_preds = model_cb.predict_proba(X_test)[:, 1]
    catboost_submission_file = "submission_catboost.csv"
    pd.DataFrame({"event_id": test_event_ids, "predict": final_preds}).to_csv(catboost_submission_file, index=False)
    log_step(f"✅ Файл {catboost_submission_file} создан.")


if __name__ == "__main__":
    main()
