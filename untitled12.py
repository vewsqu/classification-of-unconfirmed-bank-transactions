PRETRAIN_FILES = [f"/content/drive/MyDrive/хакатон3/pretrain_part_{i}.parquet" for i in range(1, 4)]
TRAIN_FILES = [f"/content/drive/MyDrive/хакатон3/train_part_{i}.parquet" for i in range(1, 4)]
TRAIN_LABELS = "/content/drive/MyDrive/хакатон3/train_labels.parquet"
PRETEST_FILE = "/content/drive/MyDrive/хакатон3/pretest.parquet"
TEST_FILE = "/content/drive/MyDrive/хакатон3/test.parquet"

def log_step(msg):
    """Утилита для логирования этапов выполнения."""
    print(f"[{datetime.datetime.now().strftime('%H:%M:%S')}]  🔹  {msg}")

def get_history_lazy(files):
    """Ленивое чтение истории транзакций для экономии памяти."""
    return pl.scan_parquet(files).select([
        "customer_id", "operaton_amt", "event_id", "event_dttm", "mcc_code", "device_system_version"
    ]).unique(subset=["event_id"], maintain_order=True).with_columns([
        pl.col("mcc_code").fill_null("-1").cast(pl.String),
        pl.col("device_system_version").fill_null("UNK").cast(pl.String)
    ])

# ==========================================
# 2. ГЕНЕРАЦИЯ ЭМБЕДДИНГОВ (WORD2VEC)
# ==========================================
def train_mcc_word2vec(vector_size=32):
    log_step("Генерация токенов (MCC + Сумма + Время)...")
    hist_lazy = pl.concat([get_history_lazy(PRETRAIN_FILES), get_history_lazy(TRAIN_FILES)])

    hist_tokens_df = hist_lazy.with_columns([
        pl.col("event_dttm").str.to_datetime().dt.hour().alias("hour"),
        pl.col("operaton_amt").fill_null(0).cast(pl.Float32)
    ]).with_columns([
        (pl.col("hour") // 6).cast(pl.String).alias("time_bin"),
        pl.col("operaton_amt").log1p().cast(pl.Int32).cast(pl.String).alias("amt_bin")
    ]).with_columns([
        pl.concat_str([pl.col("mcc_code"), pl.col("amt_bin"), pl.col("time_bin")], separator="_").alias("complex_token")
    ]).select(["customer_id", "complex_token", "event_dttm"]).collect()

    hist_tokens_df = hist_tokens_df.sort(["customer_id", "event_dttm"])
    sentences = hist_tokens_df.group_by("customer_id", maintain_order=True).agg(pl.col("complex_token"))["complex_token"].to_list()

    log_step(f"Обучение Word2Vec (размер вектора: {vector_size})...")
    w2v_model = Word2Vec(sentences, vector_size=vector_size, window=7, min_count=1, workers=4, sg=1, negative=7, epochs=5, seed=42)
    vocab = list(w2v_model.wv.index_to_key)
    vectors = [w2v_model.wv[word] for word in vocab]
    del sentences; gc.collect()

    w2v_cols = [f"token_w2v_{i}" for i in range(vector_size)]
    w2v_df = pl.DataFrame({
        "complex_token": vocab,
        **{w2v_cols[i]: [float(v[i]) for v in vectors] for i in range(vector_size)}
    }).select([pl.col("complex_token")] + [pl.col(c).cast(pl.Float32) for c in w2v_cols])

    user_w2v_cols = [f"user_profile_w2v_{i}" for i in range(vector_size)]
    user_profile_df = hist_tokens_df.lazy().join(w2v_df.lazy(), on="complex_token", how="inner").group_by("customer_id").agg([
        pl.col(w2v_cols[i]).mean().cast(pl.Float32).alias(user_w2v_cols[i]) for i in range(vector_size)
    ]).collect()
    del hist_tokens_df; gc.collect()

    return w2v_df.lazy(), w2v_cols, user_profile_df.lazy(), user_w2v_cols

